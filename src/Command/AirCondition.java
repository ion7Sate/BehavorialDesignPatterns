package Command;

public class AirCondition {
    public void powerOn(){
        System.out.println("AirCondition : Power source connected");
    }

    public void setTemperature(){
        System.out.println("AirCondition : Default Temperature set");
    }

    public void start(){
        System.out.println("AirCondition  is started");
    }

    public void powerOff(){
        System.out.println("AirCondition  is switched off");
    }
}
